    /*
     Simple instructions how to use provided Parallax Script
     ----------------------------------------------------
     
     Step 1: Create a empty gameobject for each layer in the parallax background. (Background1, Background2 etc.)
     Step 2: Create three or more child gameobjects in each background gameobject. Add a sprite renderer to each child. (1, 2, 3, ...)
     Step 3: Child 1 should be the left most background sprite and the highest number child the right most background sprite.
     Step 4: Set the sprites width in each backgrounds parallax script. The forest sprites are 256x256px with 100px per unit -> 2.56 width.
     Step 5: Set whatever parallax speed you think looks good and your are all set.

    */
}
